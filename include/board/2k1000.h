#pragma once

#ifdef LOONGARCH

#include <types.h>

// ——— UART（兼容 NS16550A）
// 2K1000 手册 14/15 章：UART 为 APB 设备挂载在 0x1fe0_xxxx 区域。
// 常见出厂映射：UART0 在 0x1fe20000（2K1000LA 手册 APB 映射）。
#ifndef LA2K1000_UART0_BASE
#define LA2K1000_UART0_BASE 0x1fe20000UL
#endif

// 板上 UART0 中断线号（IOINTC 索引）。通常为 2（参考现有内核与手册范例）。
#ifndef LA2K1000_UART0_IRQ
#define LA2K1000_UART0_IRQ 2
#endif

// ——— IOCSR EXTIOI（路由/屏蔽在 IOCSR，见手册 IOCSR 表）
#define LA2K1000_IOCSR_EXTIOI_EN_BASE 0x1600
#define LA2K1000_IOCSR_EXTIOI_ISR_BASE 0x1800
#define LA2K1000_IOCSR_EXTIOI_MAP_BASE 0x14c0
#define LA2K1000_IOCSR_EXTIOI_ROUTE_BASE 0x1c00
#define LA2K1000_IOCSR_EXRIOI_NODETYPE_BASE 0x14a0

// ——— IOINTC（2K1000 芯片级 IO 中断控制器，手册 9 章 0x1fe014xx）
#define LA2K1000_IOINTC_BASE 0x1fe01400UL
#define LA2K1000_INTISR_0 (LA2K1000_IOINTC_BASE + 0x20)   // 低 32 位状态
#define LA2K1000_INTIEN_0 (LA2K1000_IOINTC_BASE + 0x24)   // 低 32 位使能状态
#define LA2K1000_INTSI_0 (LA2K1000_IOINTC_BASE + 0x28)    // 低 32 位设置使能
#define LA2K1000_INTCLR_0 (LA2K1000_IOINTC_BASE + 0x2c)   // 低 32 位清除/脉冲清除
#define LA2K1000_INTPOL_0 (LA2K1000_IOINTC_BASE + 0x30)   // 低 32 位极性（0高有效/1低有效）
#define LA2K1000_INTEDGE_0 (LA2K1000_IOINTC_BASE + 0x34)  // 低 32 触发（1 脉冲/0 电平）

#define LA2K1000_ENTRY32_0 (LA2K1000_IOINTC_BASE + 0x40)  // 8×4B，为 0..31 路由
#define LA2K1000_ENTRY32_1 (LA2K1000_IOINTC_BASE + 0x44)  // 32..39
#define LA2K1000_ENTRY32_2 (LA2K1000_IOINTC_BASE + 0x48)  // 40..47
#define LA2K1000_ENTRY32_3 (LA2K1000_IOINTC_BASE + 0x4c)  // 48..55
#define LA2K1000_ENTRY32_4 (LA2K1000_IOINTC_BASE + 0x50)  // 56..63

#define LA2K1000_INTISR_1 (LA2K1000_IOINTC_BASE + 0x60)  // 高 32 位状态
#define LA2K1000_INTIEN_1 (LA2K1000_IOINTC_BASE + 0x64)
#define LA2K1000_INTSI_1 (LA2K1000_IOINTC_BASE + 0x68)
#define LA2K1000_INTCLR_1 (LA2K1000_IOINTC_BASE + 0x6c)
#define LA2K1000_INTPOL_1 (LA2K1000_IOINTC_BASE + 0x70)
#define LA2K1000_INTEDGE_1 (LA2K1000_IOINTC_BASE + 0x74)

// 统一几个常用外设的 IRQ 索引（与当前代码习惯保持一致）
#ifndef LA2K1000_PCIE_IRQ
#define LA2K1000_PCIE_IRQ 32
#endif

// 可选：ECAM/IO 内存窗口（若后续启用 PCIe，可参考固件窗口设置）
#define LA2K1000_PCIE0_ECAM 0x20000000UL  // 由固件映射
#define LA2K1000_PCIE0_MMIO 0x40000000UL  // 由固件映射

#endif  // LOONGARCH
